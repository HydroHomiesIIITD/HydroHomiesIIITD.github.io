function initMap() {
  map = new google.maps.Map(document.getElementById("map"), {
    zoom: 5,
    center: { lat: 28.7041, lng: 77.1025 },
    mapId: "cca9f48a987b1341",
  });

  for (const city in citymap) {
    var cityColor= "#FFFFFF";
    if (citymap[city].weight<10) {
      cityColor= "#006400";
    } else if (citymap[city].weight>20) {
      cityColor= "#640000";
    } else {
      cityColor= "#AAAA00";
    }
    const cityCircle = new google.maps.Circle({
      strokeColor: "#FFFFFF",
      strokeOpacity: 0.4,
      strokeWeight: 2,
      fillColor: cityColor,
      fillOpacity: 0.5,
      map,
      center: citymap[city].center,
      radius: Math.sqrt(citymap[city].weight) * 6000,
    });
  }
}
